import { app, BrowserWindow, ipcMain } from 'electron';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import { connectToFtp, downloadPdfFiles, closeFtpConnection } from './services/ftpService';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

ipcMain.handle('connect-to-ftp', async (_event, config) => {
    const result = await connectToFtp(config);
    console.log('Resultado de la conexiÃ³n FTP:', result);
    return result;
});

ipcMain.handle('download-pdf-files', async () => {
    const result = await downloadPdfFiles();
    console.log('Resultado de la descarga de archivos PDF:', result);
    return result;
});

ipcMain.handle('close-ftp-connection', () => {
    closeFtpConnection();
    return { success: true, message: 'ConexiÃ³n FTP cerrada' };
});

